package com.neotropic.prototype.ssh.entities;


/**
 * Default entity to represent data got it from ssh
 * @author Adrian Martinez Molina {@literal <adrian.martinez@kuwaiba.org>}
 */
public class DefaultEntity {
    /**
     * A field to keep a track of a line
     */
    private String line;

    public DefaultEntity(String line) {
        this.line = line;
    }

    public String getLine() {
        return line;
    }

    public void setLine(String line) {
        this.line = line;
    }
    
    
}
