package org.kuwaiba.n4jdriver.model;

/**
 * An entity class used to demonstrate the process of persisting business objects.
 * @author Charles Edward Bedon Cortazar {@literal <charles.bedon@kuwaiba.org>}
 */
public class Person {
    /**
     * Person id.
     */
    private long id;
    /**
     * Person name.
     */
    private String name;

    public Person(long id, String name) {
        this.id = id;
        this.name = name;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
